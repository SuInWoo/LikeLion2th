package course1005;

/**
 * plus
 * minus
 * multiple
 * divide
 * 파라미터는 int a,b
 *
 * psvm 생성 x
 */
public class Calculator {
    public void plus(int a, int b) {
        System.out.println(a + b);
    }

    public void minus(int a, int b) {
        System.out.println(a - b);
    }

    public void multiple(int a, int b) {
        System.out.println(a * b);
    }

    public void divide(float a, float b) {
        System.out.println(a / b);
    }
}
